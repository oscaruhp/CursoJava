package cursojavaincrementodecremento;

/**
 *
 * @author oscaruh
 */
public class CursoJavaIncrementoDecremento {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       
       
      
        int NumeroImprimir;
        NumeroImprimir=5;
        // incremento en 1
        NumeroImprimir++;
        System.out.println("Valor del número :"+NumeroImprimir);
        // decremento en 1
        NumeroImprimir--;
        System.out.println("Valor del número :"+NumeroImprimir);
        // incremento en 1
        ++NumeroImprimir;
        System.out.println("Valor del número :"+NumeroImprimir);
        // decremento en 1
        --NumeroImprimir;
        System.out.println("Valor del número :"+NumeroImprimir);
      
        // incremento en n=5
        NumeroImprimir+=5;
        System.out.println("Valor del número :"+NumeroImprimir);
        // decremento en n=5
        NumeroImprimir-=5;
        System.out.println("Valor del número :"+NumeroImprimir);
        // multiplicación incremental   
        NumeroImprimir*=4;
        System.out.println("Valor del número :"+NumeroImprimir);   
         
    }
    
}
